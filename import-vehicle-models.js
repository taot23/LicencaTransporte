// Processar os dados
const lines = vehicleData.trim().split('\n');
const vehicles = [];

lines.forEach(line => {
  // Usar regex para dividir por múltiplos espaços ou tabs
  const parts = line.split(/\s{2,}|\t/);
  if (parts.length >= 2) {
    const brand = parts[0].trim();
    const model = parts.slice(1).join(' ').trim();
    
    // Determinar o tipo de veículo baseado na marca/modelo
    let vehicleType = 'truck'; // padrão
    
    // Classificar por tipo baseado na marca e modelo
    if (brand.includes('IMO') || brand.includes('I/MO') || brand.includes('MO/SANY')) {
      vehicleType = 'crane';
    } else {
      vehicleType = 'tractor_unit'; // A maioria são unidades tratoras
    }
    
    vehicles.push({
      brand,
      model,
      vehicleType
    });
  }
});

console.log(`Processando ${vehicles.length} veículos...`);

// Gerar comandos SQL
const sqlCommands = vehicles.map(vehicle => {
  return `INSERT INTO vehicle_models (brand, model, vehicle_type) VALUES ('${vehicle.brand.replace(/'/g, "''")}', '${vehicle.model.replace(/'/g, "''")}', '${vehicle.vehicleType}');`;
});

// Salvar em arquivo SQL
fs.writeFileSync('vehicle-models-insert.sql', sqlCommands.join('\n'));

